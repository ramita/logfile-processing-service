package com.creditsuisse.src.model;

/**
 * Logger Data.
 */
public class LoggerData {
    private String id;
    private boolean alert;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public boolean isAlert() {
        return alert;
    }

    public void setAlert(boolean alert) {
        this.alert = alert;
    }
}
